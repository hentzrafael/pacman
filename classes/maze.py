        for line in range(self.size):
            for column in range(self.size):
                tile = self.tiles[line][column]
                cointile = self.coinTiles[line][column]
                if tile > 0:
                    x = column * Maze.SQUARE_SIZE
                    y = line * Maze.SQUARE_SIZE
                    self.drawer.rect(self.screen, Maze.BLUE, [x, y, Maze.SQUARE_SIZE, Maze.SQUARE_SIZE], 0)
                    if cointile == 1:
                        self.drawer.circle(self.screen, Maze.WHITE, (x + mid_square, y + mid_square), 2, 0)

        x = self.pacman.x * Maze.SQUARE_SIZE
        y = self.pacman.y * Maze.SQUARE_SIZE
        self.addPoints()
        self.screen.blit(self.pacman.image,[y,x])
    
